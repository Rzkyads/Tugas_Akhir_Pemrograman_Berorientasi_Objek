    private void cariButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariButtonActionPerformed
        // TODO add your handling code here:
        String angka = nonimTextField.getText();
        model.setRowCount(0);
        
       try{
            openDb();
             rs=s.executeQuery("SELECT user.nama,transaksi.nim,transaksi.tanggal,transaksi.masuk FROM transaksi INNER JOIN user on user.nim=transaksi.nim WHERE transaksi.nim="+angka+";");
             
             while(rs.next()){
                Object[]row={
                    
                    rs.getString("nim"),
                    rs.getString("tanggal"),
                    rs.getInt("masuk"),
                       
                };
            model.addRow(row);
            TextNama.setText(rs.getString("nama"));
            }
            rs=s.executeQuery("SELECT SUM(masuk) AS total FROM transaksi WHERE nim="+angka+";");
            rs.next();
            totalSaldo.setText(rs.getString("total"));
         }catch(SQLException | ClassNotFoundException ex){
            ex.printStackTrace(); 
         }
    }//GEN-LAST:event_cariButtonActionPerformed

