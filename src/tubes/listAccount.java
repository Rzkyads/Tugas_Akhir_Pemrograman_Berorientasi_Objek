    private static void openDb()throws ClassNotFoundException, SQLException{
        String URL = "jdbc:mysql://localhost:3306/db_transaksi";
        String USERNAME = "root";
        String PASSWORD = "";
        
        Class.forName("com.mysql.cj.jdbc.Driver");
        c = DriverManager.getConnection(URL,USERNAME,PASSWORD);
        s = c.createStatement();
    }
    private static void closeDb(){
        try{
            rs.close();
            s.close();
            c.close();
        }catch(SQLException e){
            e.printStackTrace();
        }
    }
    
    private void setupTable(){
        model.addColumn("NIM");
        model.addColumn("Tanggal");
        model.addColumn("Saldo Masuk");
        
        
         try{
             openDb();
             rs=s.executeQuery("SELECT * FROM transaksi ;");
             
             while(rs.next()){
                Object[]row={
                    rs.getString("nim"),
                    rs.getString("tanggal"),
                    rs.getInt("masuk"),
                       
                };
            model.addRow(row);
            
            }
         }catch(SQLException | ClassNotFoundException ex){
            ex.printStackTrace(); 
         }
    }
    private void search(){
        
         try{
             openDb();
             rs=s.executeQuery("SELECT user.nama,transaksi.nim,transaksi.tanggal,transaksi.masuk FROM transaksi INNER JOIN user on user.nim=transaksi.nim;");
             
             while(rs.next()){
                Object[]row={
                    
                    rs.getString("nim"),
                    rs.getString("tanggal"),
                    rs.getInt("masuk"),
                       
                };
            model.addRow(row);
            
            }
         }catch(SQLException | ClassNotFoundException ex){
            ex.printStackTrace(); 
         }
    }
